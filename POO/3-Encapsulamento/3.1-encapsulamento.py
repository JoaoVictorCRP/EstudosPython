#                                       O QUE É ENCAPSULAMENTO?
#  O encapsulamento é um dos conceitos fundamentais em POO, ele descreve a ideia de agrupar dados e os métodos que manipulam esses
#  dados em uma unidade. Isso impõe restriuções ao acesso direto a variáveis e métodos e pode evitar a modificação acidental de dados
#  Para evitar alterações acidentais, a variável de um objeto só pode ser alterada pelo método desse objeto.

# Encapsulamento --> Proteção de dados de um objeto.