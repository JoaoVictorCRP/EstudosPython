# A palavra polimorfismo significa ter muitas formas. Na programação, polimorfismo significa o mesmo nome de função (mas assinaturas
# diferentes) sendo usado para tipos diferentes.

# Podemos ver um exemplo de polimorfismo com o método len()

print(len("python")) # Retorna 6, pois realizou a contagem das letras da string
print(len([10, 20, 30])) # Retorna 3, pois realizou a contagem dos itens da lista.